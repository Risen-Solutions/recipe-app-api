

def add(x, y):
    """
    Add two numbers together
    :param x: Number 1
    :param y: Number 2
    :return: Sum of number 1 & 2
    """
    return x + y


def subtract(x, y):
    """
    Subtract x from y
    :param x: number to subtract
    :param y: number to subtract from
    :return: result
    """
    return y - x
